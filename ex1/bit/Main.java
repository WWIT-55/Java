package ex1.bit;

import java.util.Scanner;

public class Main {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int test = sc.nextInt();
		
	

		//입력
		for(int i=0;i<5;i++)
			for(int j=0;j<test-1;j++);
		
		
		//출력
			System.out.print("*");
			System.out.println();
				
	}

}
